  function makeChain()
  {
    var camBlock = new Shape(svgToCgo2D(CamOutline, -wheelCx, -wheelCy), {fillColor:fil1, border:true}),
        cam = new Shape(svgToCgo2D(ChainCam, -wheelCx, -wheelCy), {fillColor:fil2, border:true}),
        link1 = new Shape(svgToCgo2D(Link, -wheelCx, -wheelCy), {fillColor:fil1, border:true}),
        link2,
        c1 = new Shape(shapeDefs.circle(2*3.0), {fillColor:fil2, border:true}),
        c2 = c1.dup(),
        hub = new Group(camBlock, cam);

    seg3 = new Shape(svgToCgo2D(ChainBarLink, -wheelCx, -wheelCy), {fillColor:fil1, border:true});
    // make a second link axle
    c2.translate(link1CxOfs, 0);
    // add axles to link1
    link1.appendPath(c1);
    link1.appendPath(c2);
    // make a copy link1 and 2 are the same
    link2 = link1.dup();
    // add axles to seg3 (the T bar link)
    seg3.appendPath(c1);
    seg3.appendPath(c2);
    // now we have links, make the chain segments
    seg2 = new Group(link2, seg3);
    seg1 = new Group(seg2, link1);

    return new Group(hub, seg1);
  }

